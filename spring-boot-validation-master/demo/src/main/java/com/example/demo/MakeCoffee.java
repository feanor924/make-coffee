package com.example.demo;

public class MakeCoffee {
	private String coffeeSize;
	private String milkSize;
	private String waterSize;
	public String getCoffeeSize() {
		return coffeeSize;
	}
	public void setCoffeeSize(String coffeeSize) {
		this.coffeeSize = coffeeSize;
	}
	public String getMilkSize() {
		return milkSize;
	}
	public void setMilkSize(String milkSize) {
		this.milkSize = milkSize;
	}
	public String getWaterSize() {
		return waterSize;
	}
	public void setWaterSize(String waterSize) {
		waterSize = waterSize;
	}
}
